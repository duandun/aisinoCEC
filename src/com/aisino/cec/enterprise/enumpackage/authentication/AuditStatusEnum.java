package com.aisino.cec.enterprise.enumpackage.authentication;

/**
 * 企业认证审核状态
 * 
 * @author zhaoTh
 */
public enum AuditStatusEnum {
    /** 待审核 */
    WAITPASS("WAITPASS", "待审核"),

    /** 审核通过 */
    PASSED("PASSED", "审核通过"),

    /** 审核未通过 */
    NOTPASS("NOTPASS", "审核未通过");

    /** 键 */
    private final String key;

    /** 键值 */
    private final String value;

    AuditStatusEnum(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }
}
