package com.aisino.cec.enterprise.enumpackage;

/**
 * 企业行业枚举
 * 
 * @author leo
 */
public enum IndustryEnum {

    INTERNET("互联网", "internet"),

    MANUFACTURE("制造", "manufacture"),

    FOOD("食品", "food");

    // 成员变量
    private final String key;

    private final String value;

    IndustryEnum(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }
}
