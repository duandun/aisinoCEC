package com.aisino.cec.product.model;

public class BinaryInfo {
    private String binaryInfoId;

    private byte[] tinyInfo;

    private byte[] standarInfo;

    private byte[] bigInfo;

    private byte[] hugeInfo;

    public String getBinaryInfoId() {
        return binaryInfoId;
    }

    public void setBinaryInfoId(String binaryInfoId) {
        this.binaryInfoId = binaryInfoId == null ? null : binaryInfoId.trim();
    }

    public byte[] getTinyInfo() {
        return tinyInfo;
    }

    public void setTinyInfo(byte[] tinyInfo) {
        this.tinyInfo = tinyInfo;
    }

    public byte[] getStandarInfo() {
        return standarInfo;
    }

    public void setStandarInfo(byte[] standarInfo) {
        this.standarInfo = standarInfo;
    }

    public byte[] getBigInfo() {
        return bigInfo;
    }

    public void setBigInfo(byte[] bigInfo) {
        this.bigInfo = bigInfo;
    }

    public byte[] getHugeInfo() {
        return hugeInfo;
    }

    public void setHugeInfo(byte[] hugeInfo) {
        this.hugeInfo = hugeInfo;
    }
}